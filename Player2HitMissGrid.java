    private void hitOrMiss(int r, int c) {
        int numShipsLeft = 1;
        if (p1Ships[r][c] == '-') {
            hitMissGrid[r][c].setBackground(new Color(255, 252, 187));
            p1Ships[r][c] = 'X';
            JOptionPane.showMessageDialog(null, "That was a MISS!");
            numShipsLeft = checkWinner();
            if (numShipsLeft != 0) {
                nextTurn();
            }
        } else if (p1Ships[r][c] != '-' && p1Ships[r][c] != 'X') {
            hitMissGrid[r][c].setBackground(new Color(255, 127, 127));
            p1Ships[r][c] = 'X';
            locationGrid[r][c].setBackground(new Color(97, 105, 110)); //Square turns gray to signify ship has been HIT
            JOptionPane.showMessageDialog(null, "That was a HIT!");
            numShipsLeft = checkWinner();
            if (numShipsLeft != 0) {
                nextTurn();
            }
        }
    }

    private void nextTurn() {
        JOptionPane.showMessageDialog(null, "REMINDER: Turn around, so Player 1 can attack.");
        if (PlayerReferences.player1HitMissinst == null) {
            PlayerReferences.player1HitMissinst = new Player1HitMissGrid();
        }
        PlayerReferences.player1HitMissinst.setVisible(true);
        if (PlayerReferences.player2HitMissinst == null) {
            PlayerReferences.player2HitMissinst = new Player2HitMissGrid();
        }
        PlayerReferences.player2HitMissinst.setVisible(false);
        if (PlayerReferences.player1inst == null) {
            PlayerReferences.player1inst = new Player1FleetLocationGrid();
        }
        PlayerReferences.player1inst.setVisible(true);
        if (PlayerReferences.player2inst == null) {
            PlayerReferences.player2inst = new Player2FleetLocationGrid();
        }
        PlayerReferences.player2inst.setVisible(false);
    }

    private EndPage gameOver;

    public int checkWinner() {
        int numShipsLeft = 0;
        for (int r = 0; r < 10; r++) {
            for (int c = 0; c < 10; c++) {
                if (p1Ships[r][c] != '-' && p1Ships[r][c] != 'X') {
                    numShipsLeft = numShipsLeft + 1;
                }
            }
        }
        if (numShipsLeft == 0) {
            int win;
            //JOptionPane.showMessageDialog(null, "Player 2 is the winner!");
            if (PlayerReferences.player2HitMissinst == null) {
                PlayerReferences.player2HitMissinst = new Player2HitMissGrid();
            }
            PlayerReferences.player2HitMissinst.setVisible(false);
            if (PlayerReferences.player2inst == null) {
                PlayerReferences.player2inst = new Player2FleetLocationGrid();
            }
            PlayerReferences.player2inst.setVisible(false);
            if (gameOver == null) {
                gameOver = new EndPage("Player 2");
            }
            gameOver.setVisible(true);
        }
        return numShipsLeft;
    }

